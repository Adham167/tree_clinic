class ProductModel {
  final String id;
  final String name;
  final String description;
  final String image;
  final double price;
  final String disease;
  final String category;

  ProductModel({
    required this.id,
    required this.name,
    required this.description,
    required this.image,
    required this.price,
    required this.disease,
    required this.category,
  });
}